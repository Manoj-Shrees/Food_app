import 'package:flutter/material.dart';

class Colours {
  static const Color primary = const Color(0xffD92330);
  static const Color accent = const Color(0xff28263E);
  static const Color background = const Color(0xffF5F3F3);
  static const Color TEXT_COLOR = accent;

  static final MaterialColor primarySwatch = _createMaterialColor(primary);

  static MaterialColor _createMaterialColor(Color color) {
    List strengths = <double>[.05];
    final swatch = <int, Color>{};
    final int r = color.red, g = color.green, b = color.blue;

    for (int i = 1; i < 10; i++) {
      strengths.add(0.1 * i);
    }
    strengths.forEach((strength) {
      final double ds = 0.5 - strength;
      swatch[(strength * 1000).round()] = Color.fromRGBO(
        r + ((ds < 0 ? r : (255 - r)) * ds).round(),
        g + ((ds < 0 ? g : (255 - g)) * ds).round(),
        b + ((ds < 0 ? b : (255 - b)) * ds).round(),
        1,
      );
    });
    return MaterialColor(color.value, swatch);
  }
}
